//import mongoose from "mongoose";
const mongoose =  require("mongoose");

const UserSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true
    },
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: false
    },
    password: {
        type: String,
        required: true
    },
    roles: [{
        type: String,
        default: "Employee"
    }],
    active: {
        type: Boolean,
        default: true
    }

});

//export const UserModel = mongoose.model("users", UserSchema);
module.exports = mongoose.model("users", UserSchema);
